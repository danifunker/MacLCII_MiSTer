	reg vs_prev = 0;
	always @(posedge clk_sys) begin
		vs_prev <= VGA_VS;
		if (VGA_VS && !vs_prev)  // Rising edge of vsync = new frame
			sim_frame_count <= sim_frame_count + 1;
	end

